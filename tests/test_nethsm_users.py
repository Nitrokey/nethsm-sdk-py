def add_users(nethsm):
    """Create a new user on the NetHSM. Tests adding every Role.

    If the real name, role or passphrase are not specified, they have to be
    specified interactively.  If the user ID is not set, it is generated by the
    NetHSM.

    This command requires authentication as a user with the Administrator
    role."""
    add_user(nethsm, C.AdministratorUser)
    add_user(nethsm, C.OperatorUser)
    add_user(nethsm, C.MetricsUser)
    add_user(nethsm, C.BackupUser)


def delete_users_not_admin(nethsm):
    user_ids = nethsm.list_users()
    if len(user_ids) > 1:
        for user_id in user_ids:
            user = nethsm.get_user(user_id=user_id.value)
            if user.user_id != "admin":
                nethsm.delete_user(user.user_id)


def change_passphrase_with_admin(nethsm, user_id):
    nethsm.set_passphrase(user_id, C.PASSPHRASE_CHANGED)


def login_user_get_state(username):
    with connect(username) as nethsm:
        nethsm.get_state()


def login_user_with_wrong_passphrase(user):
    with connect(user) as nethsm:
        with pytest.raises(nethsm_module.NetHSMError):
            nethsm.get_user(user_id=user.USER_ID)


def add_operator_tags(nethsm):
    nethsm.add_operator_tag(user_id=C.OperatorUser.USER_ID, tag=C.TAG1)
    nethsm.add_operator_tag(user_id=C.OperatorUser.USER_ID, tag=C.TAG2)
    nethsm.add_operator_tag(user_id=C.OperatorUser.USER_ID, tag=C.TAG3)


