def test_list_get_delete_add_users(nethsm):

    delete_users_not_admin(nethsm)
    add_users(nethsm)
    user_ids = nethsm.list_users()
    remaining = C.USERS_LIST.copy()
    for user_id in user_ids:
        user = nethsm.get_user(user_id=user_id)

        for i in range(len(remaining)):
            if user.user_id == remaining[i].USER_ID:
                assert user.real_name == remaining[i].REAL_NAME
                assert user.role.value == remaining[i].ROLE
                remaining.pop(i)
                break

    assert remaining == []


def test_get_user_admin(nethsm):
    """Query the real name and role for a user ID on the NetHSM.

    This command requires authentication as a user with the Administrator or
    Operator role."""
    user = nethsm.get_user(user_id=C.AdminUser.USER_ID)
    assert user.user_id == C.AdminUser.USER_ID
    assert user.real_name == C.AdminUser.REAL_NAME
    assert user.role.value == C.AdminUser.ROLE


